#Lists in Python

#1. Homogenous Lists
friends = ["Rolf","Rob","Anne"]
print(friends[0])
print(friends[1])
print("\n")

#2.Lists of lists
people = [["Rolf",23],
          ["Anne",25],
          ["Jessie",43]]
print(people[1][0])     #accessing second name.
print(people[2][1])     #accessing age of Jessie
